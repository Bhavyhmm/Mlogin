public class PostClass {

    byte[] uimage, pimage;
    String pid, uname, pdatetime, pdesc, pcmnt;

    PostClass(String pid, byte[] uimage, byte[] pimage, String uname, String pdatetime, String pdesc, String pcmnt) {
        this.pid = pid;
        this.uimage = uimage;
        this.pimage = pimage;
        this.uname = uname;
        this.pdatetime = pdatetime;
        this.pdesc = pdesc;
        this.pcmnt = pcmnt;

    }

    public byte[] getUimage() {
        return uimage;
    }

    public void setUimage(byte[] uimage) {
        this.uimage = uimage;
    }

    public byte[] getPimage() {
        return pimage;
    }

    public void setPimage(byte[] pimage) {
        this.pimage = pimage;
    }

    public String getPid() {
        return pid;
    }

    public void setPid(String pid) {
        this.pid = pid;
    }

    public String getUname() {
        return uname;
    }

    public void setUname(String uname) {
        this.uname = uname;
    }

    public String getPdatetime() {
        return pdatetime;
    }

    public void setPdatetime(String pdatetime) {
        this.pdatetime = pdatetime;
    }

    public String getPdesc() {
        return pdesc;
    }

    public void setPdesc(String pdesc) {
        this.pdesc = pdesc;
    }

    public String getPcmnt() {
        return pcmnt;
    }

    public void setPcmnt(String pcmnt) {
        this.pcmnt = pcmnt;
    }
}



























































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































